			<section>
				<div class="page-header">
					<h1>Our Concerns</h1>
				</div>
				<p>Despite the enthusiastic private entities 
					to participate in PPP and huge infrastructure needs 
					in the country, we still see the missing middle 
					between the procedure and transparent mechanism 
					to reach the dealt.</p>
				<p>IPKC believes the transformation of the government 
					in terms of good governance, transparency, and 
					accountability will attract more investors to be 
					interested in the country's PPP program.</p>
				<p>We are confident that by pulling to that 
					direction, doing business in Indonesia is indeed 
					a very good business, and more opportunities 
					to come.</p>
				<p>By doing so, we all may ensure that what government 
					put forward in the market is what we want 
					and something that the private sectors can 
					actually provide.</p>
			</section>

