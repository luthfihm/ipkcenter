					<h1>Brief History</h1>
				</div>
				<p>Infrastructure Partnership and Knowledge Center (IPKC)
					was established on 28 October 2012, as a forum of 
					communication, coordination, and constultation for
					the Indonesian Infrastructure sector.</p>
				<p>This is a result of a long round table where academics 
					professional and some key players in the industries 
					expected that there should be a unique table where 
					key players in infrastructure can gather together 
					, discuss, and assess with a balanced judgement on 
					infrastructure development, Public-Private Partnership 
					market enhancement, and opportunities.</p>
			</section>

