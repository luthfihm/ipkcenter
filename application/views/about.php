			<section>
				<div class="page-header">
					<h1>Who Are We</h1>
				</div>
				<p>IPKC is a non-profit organization which activities 
					involce formulating opinions and ideas to develop 
					the infrastructure in Indonesia, at the national 
					as well as international levels toward a self reliance 
					industry characterized by the motto: "coordination for implementation"</p>
				<p>IPKC members comprise of all stakeholders in infrastructure 
					sector from the upstream to the downstream, covering the entire 
					value chain of activities</p>
				<p>IPKC covers all elements of individuals, associations, companies, 
					cooperatives, and academics, nationals as well as international, 
					which are directly associated with infrastructure sector 
					activities in Indonesia.</p>	
			</section>

